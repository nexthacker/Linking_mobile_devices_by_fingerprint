class Mac:
    def __init__():
        self.ssid = []
        self.mac_adress = None

    def add_ssid(new_ssid):
        self.ssid.append(new_ssid)

    def set_adress(new_adress):
        self.mac_adress = new_adress

    def get_ssids():
        return self.ssid

    def get_adress():
        return self.mac_adress

